//Fonction remplir toute les case
function tiles_continue() {
    $(".case").hover(function () {
        if (selected_texture != "" && document.getElementById("tiles_continue").checked == true) {
            $(this).html("<img src='" + selected_texture + "'>");
        }
    });
};

//Fonction case par case
function tiles_par_tiles() {
    $(".case").click(function (e) {
        e.preventDefault();
        if (selected_texture != "" && document.getElementById("tiles_par_tiles").checked == true) {
            $(this).html("<img src='" + selected_texture + "'>");
            $(".case").css("cursor", "");
        }
    });
};

//Fonction collision
function collision(target) {
    $(".case").click(function (e) {
        let type_collision = target.name;
        let id_case = e.target.id;
        switch (type_collision) {
            case "ch":
                e.preventDefault();
                if (document.getElementById("ch").checked == true) {
                    $(this).addClass("ch");
                    $(".dimensional_case_"+id_case).text("'CH',");
                    $(".case").css("cursor", "");
                }
                break;
            case "cb":
                e.preventDefault();
                if (document.getElementById("cb").checked == true) {
                    $(this).addClass("cb");
                    $(".dimensional_case_"+id_case).text("'CB',");
                    $(".case").css("cursor", "");
                }
                break;
            case "cd":
                e.preventDefault();
                if (document.getElementById("cd").checked == true) {
                    $(this).addClass("cd");
                    $(".dimensional_case_"+id_case).text("'CD',");
                    $(".case").css("cursor", "");
                }
                break;
            case "cg":
                e.preventDefault();
                if (document.getElementById("cg").checked == true) {
                    $(this).addClass("cg");
                    $(".dimensional_case_"+id_case).text("'CG',");
                    $(".case").css("cursor", "");
                }
                break;
            case "chd":
                e.preventDefault();
                if (document.getElementById("chd").checked == true) {
                    $(this).addClass("chd");
                    $(".dimensional_case_"+id_case).text("'CHD',");
                    $(".case").css("cursor", "");
                }
                break;
            case "cbd":
                e.preventDefault();
                if (document.getElementById("cbd").checked == true) {
                    $(this).addClass("cbd");
                    $(".dimensional_case_"+id_case).text("'CBD',");
                    $(".case").css("cursor", "");
                }
                break;
            case "chg":
                e.preventDefault();
                if (document.getElementById("chg").checked == true) {
                    $(this).addClass("chg");
                    $(".dimensional_case_"+id_case).text("'CHG',");
                    $(".case").css("cursor", "");
                }
                break;
            case "cbg":
                e.preventDefault();
                if (document.getElementById("cbg").checked == true) {
                    $(this).addClass("cbg");
                    $(".dimensional_case_"+id_case).text("'CBG',");
                    $(".case").css("cursor", "");
                }
                break;
            default:
                break;
        }
    });
}


